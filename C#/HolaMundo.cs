using System;

class Program
// Estructura fundamental que mezcla datos con atributos y métodos(comportamiento)
// Objetos: Instancias de clases
{
    static void Main() 
    // static: indica que pertenece a la clase, no instancias
    // void: no retorna ningún valor 
    // Main: método especial de C# como punto de entrada de aplicación de consola 
    {
        Console.WriteLine("Hola Mundo!");
        Console.ReadLine();
        // El ReadLine permite que no se cierre de inmediato al leer entrada de usuario
    }
}